package calcul;

public class Operation {
    public float addition(float add, float add1) {
        return add + add1;
    }

    public float soustraction(float add, float add1) {
        return add - add1;
    }

    public float multiplication(float add, float add1) {
        return add * add1;
    }

    public float division(float add, float add1) {
        return add / add1;
    }

}
